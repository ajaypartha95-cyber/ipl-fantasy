drop table if exists public.team_match_points cascade;
drop table if exists public.player_match_stats cascade;
drop table if exists public.player_match_points cascade;
drop table if exists public.leaderboard cascade;
drop table if exists public.fantasy_team_players cascade;
drop table if exists public.fantasy_teams cascade;
drop table if exists public.matches cascade;
drop table if exists public.players cascade;
drop table if exists public.profiles cascade;
drop table if exists public.seasons cascade;

create table public.profiles (
  id bigint generated by default as identity primary key,
  username text not null unique,
  display_name text not null,
  is_admin boolean not null default false,
  substitutions_left int4 not null default 20,
  created_at timestamptz not null default now(),
  email text not null unique
);

create table public.seasons (
  id bigint generated by default as identity primary key,
  name text not null,
  year integer not null,
  is_active boolean not null default true,
  created_at timestamptz not null default now()
);

create table public.players (
  id bigint generated by default as identity primary key,
  name text not null,
  role text not null,
  ipl_team text not null,
  season_id bigint not null references public.seasons(id) on delete cascade,
  created_at timestamptz not null default now()
);

create table public.fantasy_teams (
  id bigint generated by default as identity primary key,
  profile_id bigint not null references public.profiles(id) on delete cascade,
  season_id bigint not null references public.seasons(id) on delete cascade,
  team_name text not null,
  captain_player_id bigint references public.players(id),
  vice_captain_player_id bigint references public.players(id),
  created_at timestamptz not null default now()
);

create table public.fantasy_team_players (
  id bigint generated by default as identity primary key,
  fantasy_team_id bigint not null references public.fantasy_teams(id) on delete cascade,
  player_id bigint not null references public.players(id) on delete cascade,
  is_active boolean not null default true,
  added_at timestamptz not null default now(),
  removed_at timestamptz null
);

create table public.matches (
  id bigint generated by default as identity primary key,
  season_id bigint not null references public.seasons(id) on delete cascade,
  match_number integer not null,
  team_1 text not null,
  team_2 text not null,
  match_date timestamptz not null,
  status text not null,
  is_group_stage boolean not null default true
);

create table public.leaderboard (
  id bigint generated by default as identity primary key,
  fantasy_team_id bigint not null references public.fantasy_teams(id) on delete cascade,
  season_id bigint not null references public.seasons(id) on delete cascade,
  total_points int4 not null default 0,
  rank int4 null,
  updated_at timestamptz not null default now()
);

create table public.player_match_points (
  id bigint generated by default as identity primary key,
  match_id bigint not null references public.matches(id) on delete cascade,
  player_id bigint not null references public.players(id) on delete cascade,
  batting_points int4 not null default 0,
  bowling_points int4 not null default 0,
  fielding_points int4 not null default 0,
  bonus_points int4 not null default 0,
  total_points int4 not null default 0,
  created_at timestamptz not null default now()
);

create table public.player_match_stats (
  id bigint generated by default as identity primary key,
  match_id bigint not null references public.matches(id) on delete cascade,
  player_id bigint not null references public.players(id) on delete cascade,
  runs int4 not null default 0,
  fours int4 not null default 0,
  sixes int4 not null default 0,
  balls_faced int4 not null default 0,
  wickets int4 not null default 0,
  maiden_overs int4 not null default 0,
  catches int4 not null default 0,
  stumpings int4 not null default 0,
  run_outs int4 not null default 0,
  created_at timestamptz not null default now()
);

create table public.team_match_points (
  id bigint generated by default as identity primary key,
  fantasy_team_id bigint not null references public.fantasy_teams(id) on delete cascade,
  match_id bigint not null references public.matches(id) on delete cascade,
  base_points int4 not null default 0,
  captain_bonus_points int4 not null default 0,
  vice_captain_bonus_points int4 not null default 0,
  total_points int4 not null default 0,
  created_at timestamptz not null default now()
);

create index idx_players_season_id on public.players(season_id);
create index idx_fantasy_teams_profile_id on public.fantasy_teams(profile_id);
create index idx_fantasy_teams_season_id on public.fantasy_teams(season_id);
create index idx_fantasy_team_players_team_id on public.fantasy_team_players(fantasy_team_id);
create index idx_fantasy_team_players_player_id on public.fantasy_team_players(player_id);
create index idx_matches_season_id on public.matches(season_id);
create index idx_leaderboard_season_id on public.leaderboard(season_id);
create index idx_player_match_points_match_id on public.player_match_points(match_id);
create index idx_player_match_points_player_id on public.player_match_points(player_id);
create index idx_player_match_stats_match_id on public.player_match_stats(match_id);
create index idx_player_match_stats_player_id on public.player_match_stats(player_id);
create index idx_team_match_points_team_id on public.team_match_points(fantasy_team_id);
create index idx_team_match_points_match_id on public.team_match_points(match_id);

create unique index idx_leaderboard_team_season_unique
  on public.leaderboard (fantasy_team_id, season_id);

create unique index idx_player_match_points_match_player_unique
  on public.player_match_points (match_id, player_id);

create unique index idx_player_match_stats_match_player_unique
  on public.player_match_stats (match_id, player_id);

create unique index idx_team_match_points_team_match_unique
  on public.team_match_points (fantasy_team_id, match_id);

